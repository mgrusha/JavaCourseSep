package com.itcourses.grusha.Objects.Carshop.Hometask;

public class Cat {

    private String name;
    private int speed;


    public Cat(String name, int speed) {
        this.name = name;
        this.speed = speed;
    }

    public String getName() {
        return name;
    }

    public int getSpeed() {
        return speed;
    }


}