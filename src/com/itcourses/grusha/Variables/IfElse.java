package com.itcourses.grusha.Variables;

public class IfElse {
    public static void main(String[] args) {
        int age = 50;

        if (age > 20) {
            System.out.println("TestObject: Age is greater than 20");
        }else
        if (age > 30){
            System.out.println("B: Age is greater than 30");
        }else
        if (age > 40){
            System.out.println("C: Age is greater than 40");
        } else
        if (age > 50){
            System.out.println("D: Age is greater than 50");
        }
    }
}
